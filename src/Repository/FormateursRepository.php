/**
 * @extends ServiceEntityRepository<Formateurs>
 *
 * @method Formateurs|null find($id, $lockMode = null, $lockVersion = null)
 * @method Formateurs|null findOneBy(array $criteria, array $orderBy = null)
 * @method Formateurs[]    findAll()
 * @method Formateurs[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class FormateursRepository extends ServiceEntityRepository implements PasswordUpgraderInterface
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Formateurs::class);
    }

    public function add(Formateurs $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Formateurs $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    /**
     * Used to upgrade (rehash) the user's password automatically over time.
     */
    public function upgradePassword(PasswordAuthenticatedUserInterface $user, string $newHashedPassword): void
    {
        if (!$user instanceof Formateurs) {
            throw new UnsupportedUserException(sprintf('Instances of "%s" are not supported.', \get_class($user)));
        }

        $user->setPassword($newHashedPassword);

        $this->add($user, true);
    }

